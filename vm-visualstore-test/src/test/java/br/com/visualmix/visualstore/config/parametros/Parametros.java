package br.com.visualmix.visualstore.config.parametros;

public enum Parametros {
	
	TESTE_SERVER_IP("TEST.IP", "localhost"),
	TESTE_SERVER_PORT("TEST.PORT", "9092"),
	TESTE_SERVER_USER("TEST.USER", "admin"),
	TESTE_SERVER_PASSWORD("TEST.PASSWORD", "1");
	
	
	public final String PARAMETER;
	public final String DEFAULT;
	
	Parametros(String pARAMETER, String dEFAULT) {
		PARAMETER = pARAMETER;
		DEFAULT = dEFAULT;
	}

	public String getParameter() {
		return PARAMETER;
	}


	public String getDefaultParameter() {
		return DEFAULT;
	}

}
